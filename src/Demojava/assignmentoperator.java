package Demojava;

public class assignmentoperator {

	public static void main(String[] args) {
		// add and assign, sub and assign, multiply and assign
		
		
		int a=100;
		a+=10;
		System.out.println(a);
		
		int b=10;
		b=b+20;
		System.out.println(b);
		
		b-=2;
		System.out.println(b);
		
		b=b*8;
		System.out.println(b);

	}

}
