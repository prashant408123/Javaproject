package Strings_method;

public class Demo1 {

	public static void main(String[] args) {
		// length method will calculate the length of string and return type is integer
		
		String a = "Selenium";
	
		System.out.println(a.length());
		
		String b = "JAVA";
		
		int c = b.length();
		
		System.out.println(c);
		
		String d = "prashant";
		System.out.println(d.length());

	}

}
