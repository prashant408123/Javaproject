package Strings_method;

public class CharAt {

	public static void main(String[] args) {
		// CharAt- it will give character as per index position and written type is char 
		
		String c = "Germany";
			System.out.println(c.charAt(4));

			String D = "sanjivani";
			System.out.println(D.charAt(5));
			
			String f = "Paris";
			char g = f.charAt(3);
			System.out.println(g);
	}

}
