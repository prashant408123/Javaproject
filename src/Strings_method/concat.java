package Strings_method;

public class concat {

	public static void main(String[] args) {
		// combination of two strings
		
		String a = "10";
		int b = 20;
		System.out.println(a+b);
		
		String d ="100";
		String e ="200";
		String f ="pune";
		System.out.println(d.concat(f)+e);
		System.out.println(d.concat(e)+f);
		
;
	}

}
